// Transmit a string via UART2
void uart_msg(char *s)
{
    // Send the given string through UART2 with 1s timeout
    HAL_UART_Transmit(&huart2, (uint8_t*)s, strlen(s), 1000);
}

// Delay function using RTOS tick count
void rtos_delay(uint32_t delay_in_ms)
{
    uint32_t tick_count_local = 0;

    // Convert delay from milliseconds to RTOS ticks
    uint32_t delay_in_ticks = (delay_in_ms * configTICK_RATE_HZ) / 1000;

    // Get current tick count
    tick_count_local = xTaskGetTickCount();

    // Busy-wait until required ticks have passed
    while (xTaskGetTickCount() < (tick_count_local + delay_in_ticks));
}



// -------- TASK-1 CODE --------
void task1(void *p)
{
    UBaseType_t p1, p2; // Variables to store priorities

    uart_msg("TASK-1 RUNNING\r\n");
    sprintf(msg, "TASK-1 priority value %ld\r\n", uxTaskPriorityGet(xTaskHandle1));
    uart_msg(msg);
    sprintf(msg, "TASK-2 priority value %ld\r\n", uxTaskPriorityGet(xTaskHandle2));
    uart_msg(msg);

    while (1)
    {
        if (switch_prio) // Check if button pressed
        {
        	switch_prio = FALSE; // Reset flag
            uart_msg("priority_switch by task-1\r\n");

            p1 = uxTaskPriorityGet(xTaskHandle1); // Get Task1 priority
            p2 = uxTaskPriorityGet(xTaskHandle2); // Get Task2 priority

            vTaskPrioritySet(xTaskHandle1, p2);   // Swap Task1 priority
            vTaskPrioritySet(xTaskHandle2, p1);   // Swap Task2 priority
        }
        else
        {
            HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_5); // Blink LED
            rtos_delay(200);                       // Short delay
        }
    }
}

